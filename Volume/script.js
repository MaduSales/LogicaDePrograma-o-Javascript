// Calcular e apresentar o volume de uma lata de óleo

let volume;
let R = parseFloat(prompt("Escreva um valor do raio da lata"));
let altura = parseFloat(prompt("Escreva um valor da altura da lata"));

volume = 3.14 * R^2 * altura
alert("volume da lata de óleo é de " + volume +" litros");

